#include "libft.h"

char    *ft_strchr(const char *s, int c)
{
    char    c2;

    c2 = (char)c;
    while (*s != '\0')
    {
        if (*s == c2)
            return ((char *)s);
        s++;
    }
    return (NULL);
}
